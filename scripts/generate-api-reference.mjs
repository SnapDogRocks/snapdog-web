import { readFile, writeFile } from 'node:fs/promises';

const specPath = 'src/assets/openapi.json';
const outputPath = 'src/content/docs/docs/api-reference.mdx';
const checkOnly = process.argv.includes('--check');
const methods = ['get', 'put', 'post', 'delete', 'patch', 'options', 'head'];

const spec = JSON.parse(await readFile(specPath, 'utf8'));

function refName(ref) {
  return ref.split('/').at(-1);
}

function headingAnchor(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

function text(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('{', '&#123;')
    .replaceAll('}', '&#125;');
}

function cell(value) {
  return text(value).replaceAll('|', '\\|').replace(/\s*\n\s*/g, ' ');
}

function codeCell(value) {
  return String(value).replaceAll('|', '\\|').replaceAll('`', '\\`');
}

function schemaType(schema) {
  if (!schema) return '—';
  if (schema.$ref) {
    const name = refName(schema.$ref);
    return `[\`${name}\`](#${headingAnchor(name)})`;
  }
  if (schema.oneOf) return schema.oneOf.map(schemaType).join(' or ');
  if (schema.anyOf) return schema.anyOf.map(schemaType).join(' or ');
  if (schema.allOf) return schema.allOf.map(schemaType).join(' & ');
  if (schema.enum) return schema.enum.map((value) => `\`${String(value)}\``).join(', ');

  const types = Array.isArray(schema.type) ? schema.type : [schema.type].filter(Boolean);
  if (types.includes('array')) return `array&lt;${schemaType(schema.items)}&gt;`;
  if (types.length > 0) return types.map((type) => `\`${type}\``).join(' or ');
  if (schema.properties) return '`object`';
  return '—';
}

function contentSchema(content) {
  if (!content) return undefined;
  const preferred = content['application/json'] ?? Object.values(content)[0];
  return preferred?.schema;
}

function successSchema(responses = {}) {
  const status = Object.keys(responses).find((code) => /^2\d\d$/.test(code));
  return status ? contentSchema(responses[status]?.content) : undefined;
}

const operations = [];
for (const [path, pathItem] of Object.entries(spec.paths ?? {})) {
  for (const method of methods) {
    const operation = pathItem[method];
    if (!operation) continue;
    operations.push({ path, method, operation });
  }
}

const declaredTags = (spec.tags ?? []).map((tag) => tag.name);
const discoveredTags = operations.flatMap(({ operation }) => operation.tags ?? ['Other']);
const tags = [...new Set([...declaredTags, ...discoveredTags])];

const lines = [
  '---',
  'title: Generated REST API Reference',
  'description: Generated endpoint and schema reference for the current SnapDog REST API contract.',
  '---',
  '',
  '{/* Generated by scripts/generate-api-reference.mjs. Do not edit manually. */}',
  '',
  'This reference is generated directly from the Rust OpenAPI registry in the',
  '[SnapDog repository](https://github.com/SnapDogRocks/snapdog). For authentication,',
  'usage guidance, and complete examples, read the [REST API guide](/docs/api-rest).',
  '',
  `Contract: **${text(spec.info?.title ?? 'SnapDog REST API')} ${text(spec.info?.version ?? '')}** · OpenAPI ${text(spec.openapi)}`,
  '',
  '## Endpoints',
  '',
];

for (const tag of tags) {
  const tagged = operations.filter(({ operation }) => (operation.tags ?? ['Other']).includes(tag));
  if (tagged.length === 0) continue;
  const tagDescription = (spec.tags ?? []).find((entry) => entry.name === tag)?.description;
  lines.push(`### ${text(tag)}`, '');
  if (tagDescription) lines.push(text(tagDescription), '');
  lines.push('| Method | Endpoint | Summary | Request | Success response |');
  lines.push('|---|---|---|---|---|');
  for (const { path, method, operation } of tagged) {
    const request = schemaType(contentSchema(operation.requestBody?.content));
    const response = schemaType(successSchema(operation.responses));
    lines.push(
      `| **${method.toUpperCase()}** | \`${codeCell(path)}\` | ${cell(operation.summary ?? operation.description ?? '—')} | ${request} | ${response} |`,
    );
  }
  lines.push('');
}

lines.push('## Schemas', '');
for (const [name, schema] of Object.entries(spec.components?.schemas ?? {}).sort(([a], [b]) => a.localeCompare(b))) {
  lines.push(`### ${text(name)}`, '');
  if (schema.description) lines.push(text(schema.description), '');

  const properties = Object.entries(schema.properties ?? {});
  if (properties.length === 0) {
    lines.push(`Type: ${schemaType(schema)}`, '');
    continue;
  }

  const required = new Set(schema.required ?? []);
  lines.push('| Field | Type | Required | Description |');
  lines.push('|---|---|:---:|---|');
  for (const [field, property] of properties) {
    lines.push(
      `| \`${codeCell(field)}\` | ${schemaType(property)} | ${required.has(field) ? 'yes' : 'no'} | ${cell(property.description || '—')} |`,
    );
  }
  lines.push('');
}

const generated = `${lines.join('\n').trim()}\n`;

if (checkOnly) {
  const current = await readFile(outputPath, 'utf8').catch(() => '');
  if (current !== generated) {
    console.error(`${outputPath} is stale. Run npm run docs:generate and commit the result.`);
    process.exit(1);
  }
  console.log(`Generated API reference is current (${operations.length} operations).`);
} else {
  await writeFile(outputPath, generated);
  console.log(`Generated ${outputPath} from ${specPath}.`);
}
